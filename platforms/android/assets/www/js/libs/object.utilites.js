//isBlankNullUndefined --- Definition
//====================================================================================================
function isBlankNullUndefined(object){
    if(object === undefined || object === null || object === "")
    {
        return true;
    }
    return false;
}
//====================================================================================================

//====================================================================================================
//isBlankNullUndefined --- Definition
//====================================================================================================
function isNotBlankNullUndefined(object){
    if(isBlankNullUndefined(object))
    {
        return false;
    }
    return true;
}
//====================================================================================================

//====================================================================================================
//isBoolean --- Definition
//====================================================================================================
function isBoolean(object){
    if(typeof object === "boolean")
    {
        return true;
    }
    return false;
}
//====================================================================================================

//====================================================================================================
//isNotBoolean --- Definition
//====================================================================================================
function isNotBoolean(object){
    if(isBoolean(object))
    {
        return false;
    }
    return true;
}
//====================================================================================================

